Line3="\\\\n\\\\\\$\\[\\$(tput sgr0)\\] \\[\\$(tput sgr0)\\]"\
PROMPT="\$(echo -e \"\$Line1 \$Line2 \$Line3 \$Line4\")"\n' bashrctest

